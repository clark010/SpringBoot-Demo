package com.clark010.test.bean;

import java.util.Date;

/**
 * 实体类
 */
public class User {
    private int id;
    private String name;
    private Date date;

    public User() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
}
